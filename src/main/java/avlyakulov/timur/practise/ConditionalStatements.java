package avlyakulov.timur.practise;

public class ConditionalStatements {
    public static void main(String[] args) {
        int a = 5;
        if (a > 0)
            System.out.println("If");
        else if (a == 5)
            System.out.println("Else if");
        else
            System.out.println("Else");
    }
}
